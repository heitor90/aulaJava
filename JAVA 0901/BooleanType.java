public class BooleanType {
    public static void main(String[] args) { 
        // criação do método executor (main)
        // static: este método não pode ser alterado não pode ser alterado
        // void: não pode retornar nenhum valor
        // String[] args: declaração de todos os paramêtros
        Boolean Verdadeiro = true; // criação da variável 'Verdadeiro' do tipo 'Boolean' com o valor de 'true' 
        Boolean Falso = false; // criação da variável 'Falso' do tipo 'Boolean' com o valor de 'false'
        System.out.println("Este é o " + Verdadeiro + " e este é o " + Falso + ".");
        // exibição no console o valor das duas variáveis criadas acima usando a concatenação '+'
    }
}
