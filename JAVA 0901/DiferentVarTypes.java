// declaração do objeto 'DiferentVarTypes'
public class DiferentVarTypes {
    public static void main(String[] args) {
        // criação do método executor (main)
        // static: este método não pode ser alterado não pode ser alterado
        // void: não pode retornar nenhum valor
        // String[] args: declaração de todos os paramêtros

        String name = "heitor"; // criação da variável 'name' do tipo 'String' com o valor "Heitor"
        float num1 = 10.90f; // criação da variável 'num1' do tipo 'float' com o valor "10.90"

        System.out.println(num1 + name); // exibição das duas variáveis concatenadas no console
    }
}
