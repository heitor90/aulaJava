// declaração do objeto 'LowerCase'
public class LowerCase {
    public static void main(String[] args) {
        // criação do método executor (main)
        // static: este método não pode ser alterado não pode ser alterado
        // void: não pode retornar nenhum valor
        // String[] args: declaração de todos os paramêtros

        String name = "HEITOR"; //  criação da variável 'name' do tipo 'String' com o valor de "Heitor"

        System.out.println(name.toLowerCase()); // exibição da variável acima usando o 'toLowerCase' para transformar todas as letras para minúsculas
    }
}
