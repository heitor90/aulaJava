// declaração do objeto 'CommaVars'
public class CommaVars {
    public static void main(String[] args) {
        // criação do método executor (main)
        // static: este método não pode ser alterado não pode ser alterado
        // void: não pode retornar nenhum valor
        // String[] args: declaração de todos os paramêtros
        String nome = "Heitor", sobrenome = "Ferreira"; // criação de duas variáveis(nome; sobrenome) com os valores "Heitor" e "Ferreira" em um mesmo dado
        // usando a ','
        
        System.out.println(nome + sobrenome); // exibição das duas variáveis usando concatenação '+'
    }
}
