// declaração do obejto 'UpperCaseString'
public class UpperCaseString {
    public static void main(String[] args) {
        // criação do método executor (main)
        // static: este método não pode ser alterado não pode ser alterado
        // void: não pode retornar nenhum valor
        // String[] args: declaração de todos os paramêtros
        String nome = "HeiToR"; // criação da variável 'nome' do tipo 'String' com o valor "HeiToR"

        System.out.println(nome.toUpperCase()); // exibição da variável 'nome' com a utilização do 'toUpperCase' deixando todas a letras
        // maiúsculas
    }
}
